name = 'Python'  # String is Immutable
number = "7"
print(name)

# Indexing
print(name[0:2])
print(name[2:4])
print(name[4:6])

# String Methods
print(name.upper())
print(name.lower())
print(name.capitalize())
print(name.count("P"))
print(name.isalnum())
print(number.isalnum())
print(number.isnumeric())
print(name.isnumeric())


