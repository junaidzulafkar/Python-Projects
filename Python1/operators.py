num1 = 10
num2 = 2

# Arithmatic Operator

print("num1 + num2 = ", num2 + num1)
print("num1 - num2 = ", num2 - num1)
print("num1 * num2 = ", num2 * num1)
print("num1 / num2 = ", num2 / num1)
print("num1 // num2 = ", num2 // num1)  # Load Division Operator
print("num1 ** num2 = ", num2 ** num1)  # Exponentiation Operator
print("num1 % num2 = ", num2 % num1)

